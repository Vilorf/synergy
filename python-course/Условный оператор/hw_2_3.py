x = abs(int(input("Введите x координату доски: ")))
y = abs(int(input("Введите y координату доски: ")))

if x <= 8 and y <= 8:
    if (x + y) % 2 == 0:
        print("Клетка шахматной доски черная")
    else:
        print("Клетка шахматной доски белая")
else:
    print("x или y выходят за рамки шахматной доски")